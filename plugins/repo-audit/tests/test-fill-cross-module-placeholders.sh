#!/usr/bin/env bash
# Tests for fill-cross-module-placeholders.sh

set -o pipefail

SCRIPT_DIR="$(cd "$(dirname "$0")" && pwd)"
REPO_ROOT="$(cd "$SCRIPT_DIR/.." && pwd)"
SCRIPT_UNDER_TEST="${REPO_ROOT}/scripts/fill-cross-module-placeholders.sh"

PASS_COUNT=0
FAIL_COUNT=0
TMPDIR=""

cleanup() {
  [ -n "$TMPDIR" ] && rm -rf "$TMPDIR"
}
trap cleanup EXIT

assert_eq() {
  local desc="$1" expected="$2" actual="$3"
  if [ "$expected" = "$actual" ]; then
    echo "  PASS: $desc"
    PASS_COUNT=$((PASS_COUNT + 1))
  else
    echo "  FAIL: $desc"
    echo "    expected: $expected"
    echo "    actual:   $actual"
    FAIL_COUNT=$((FAIL_COUNT + 1))
  fi
}

assert_contains() {
  local desc="$1" needle="$2" haystack="$3"
  if echo "$haystack" | grep -q "$needle"; then
    echo "  PASS: $desc"
    PASS_COUNT=$((PASS_COUNT + 1))
  else
    echo "  FAIL: $desc"
    echo "    expected to contain: $needle"
    echo "    actual: $haystack"
    FAIL_COUNT=$((FAIL_COUNT + 1))
  fi
}

assert_not_contains() {
  local desc="$1" needle="$2" haystack="$3"
  if echo "$haystack" | grep -q "$needle"; then
    echo "  FAIL: $desc"
    echo "    expected NOT to contain: $needle"
    echo "    actual: $haystack"
    FAIL_COUNT=$((FAIL_COUNT + 1))
  else
    echo "  PASS: $desc"
    PASS_COUNT=$((PASS_COUNT + 1))
  fi
}

# Helper to set up a project dir with reports containing placeholders
setup_project_with_placeholders() {
  TMPDIR=$(mktemp -d)
  mkdir -p "$TMPDIR/sdlc-audit/reports"
  mkdir -p "$TMPDIR/sdlc-audit/data"

  # AUDIT_REPORT.md with placeholder
  cat > "$TMPDIR/sdlc-audit/reports/AUDIT_REPORT.md" <<'REPORT'
# Audit Report

**Total findings: 5** (1 critical, 2 warning, 2 info)

## Critical Findings (1)

- **src/auth.js**: Hardcoded secret

<!-- CROSS_MODULE_PLACEHOLDER -->

---
*Generated by repo-audit*
REPORT

  # TECH_DEBT.md with placeholders
  cat > "$TMPDIR/sdlc-audit/reports/TECH_DEBT.md" <<'REPORT'
# Tech Debt Backlog

**10 issues** identified, prioritized by effort and impact.

## Strategic Improvements
*Medium effort, high impact — plan these into sprints.*

<!-- DRY_VIOLATIONS_PLACEHOLDER -->

- **src/utils.js** (maintainability): Long function

## Major Refactors
*Large effort, high impact — plan carefully.*

<!-- ARCHITECTURE_ISSUES_PLACEHOLDER -->

- **src/core.js** (architecture): God module

---
*Generated by repo-audit*
REPORT

  # DEPENDENCY_GRAPH.md with placeholder
  cat > "$TMPDIR/sdlc-audit/reports/DEPENDENCY_GRAPH.md" <<'REPORT'
# Dependency Graph

**5 modules**, 10 external dependencies, 1 circular dependency

## Internal Dependencies

```
src/auth → src/utils [in:0 out:1]
```

<!-- DEP_GRAPH_INTERPRETATION_PLACEHOLDER -->

---
*Generated by repo-audit*
REPORT

  # TEST_COVERAGE_MAP.md with placeholder
  cat > "$TMPDIR/sdlc-audit/reports/TEST_COVERAGE_MAP.md" <<'REPORT'
# Test Coverage Map

## Coverage by Module

| Module | Coverage |
|--------|----------|
| src/auth | none |

<!-- COVERAGE_GAPS_PLACEHOLDER -->

---
*Generated by repo-audit*
REPORT
}

# Helper to create cross-module JSON files
create_cross_module_data() {
  local data_dir="$TMPDIR/sdlc-audit/data"

  cat > "$data_dir/cross-module-dry.json" <<'JSON'
{
  "duplications": [
    {
      "description": "Config parsing duplicated",
      "locations": ["src/auth/config.js", "src/api/config.js"],
      "suggestion": "Extract shared config parser into src/common/config.js"
    },
    {
      "description": "Validation logic repeated",
      "locations": ["src/users/validate.js", "src/orders/validate.js", "src/products/validate.js"],
      "suggestion": "Create a shared validation module"
    }
  ]
}
JSON

  cat > "$data_dir/cross-module-inconsistencies.json" <<'JSON'
{
  "inconsistencies": [
    {
      "pattern_type": "Error handling",
      "description": "Mix of throw and return-error patterns",
      "examples": ["src/auth throws", "src/api returns errors"],
      "recommendation": "Standardize on throw with try-catch"
    }
  ]
}
JSON

  cat > "$data_dir/cross-module-architecture.json" <<'JSON'
{
  "architecture_issues": [
    {
      "type": "God module",
      "description": "src/core handles too many responsibilities",
      "affected_modules": ["src/core", "src/auth", "src/api"],
      "suggestion": "Split into domain-specific modules"
    }
  ],
  "dependency_interpretation": {
    "problematic_cycles": [
      {
        "cycle": ["src/auth", "src/core", "src/auth"],
        "reason": "Auth depends on core for config, core depends on auth for middleware"
      }
    ],
    "hub_assessment": [
      {
        "module": "src/core",
        "assessment": "High fan-in is justified as shared utilities but should be split"
      }
    ],
    "decoupling_suggestions": [
      "Extract config from src/core into a standalone module",
      "Use dependency injection for auth middleware"
    ]
  },
  "duplicate_externals": [
    {
      "package": "lodash",
      "issue": "Used in 4 modules but only for _.get — replace with optional chaining"
    }
  ]
}
JSON

  cat > "$data_dir/cross-module-coverage.json" <<'JSON'
{
  "test_gaps": [
    {
      "module": "src/auth",
      "priority": 1,
      "description": "No tests for authentication module",
      "risk_note": "Highest risk module with no coverage"
    },
    {
      "module": "src/payments",
      "priority": 2,
      "description": "No tests for payment processing",
      "risk_note": "Financial operations need thorough testing"
    }
  ]
}
JSON
}

