# Check DEPENDENCY_GRAPH.md
DEP_CONTENT=$(cat "$TMPDIR/sdlc-audit/reports/DEPENDENCY_GRAPH.md")
assert_not_contains "Dep graph placeholder removed" "DEP_GRAPH_INTERPRETATION_PLACEHOLDER" "$DEP_CONTENT"
assert_contains "Dep graph has interpretation section" "Dependency Interpretation" "$DEP_CONTENT"
assert_contains "Dep graph has problematic cycles" "Problematic Cycles" "$DEP_CONTENT"
assert_contains "Dep graph has hub assessment" "Hub Assessment" "$DEP_CONTENT"
assert_contains "Dep graph has decoupling suggestions" "Decoupling Suggestions" "$DEP_CONTENT"
assert_contains "Dep graph has duplicate externals" "Duplicate External Dependencies" "$DEP_CONTENT"
assert_contains "Dep graph has lodash issue" "lodash" "$DEP_CONTENT"

# Check TEST_COVERAGE_MAP.md
COV_CONTENT=$(cat "$TMPDIR/sdlc-audit/reports/TEST_COVERAGE_MAP.md")
