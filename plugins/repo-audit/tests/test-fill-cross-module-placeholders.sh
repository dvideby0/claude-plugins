assert_not_contains "CROSS_MODULE placeholder removed" "CROSS_MODULE_PLACEHOLDER" "$AUDIT_CONTENT"
assert_not_contains "No cross-module section added" "Cross-Module Analysis" "$AUDIT_CONTENT"
